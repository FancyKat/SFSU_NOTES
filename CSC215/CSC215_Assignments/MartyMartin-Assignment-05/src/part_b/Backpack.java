package part_b;

public class Backpack {
    private final String color;
    private final int size;

    // Constructor, getters, and setters
    public Backpack(String color, int size) {
        this.color = color;
        this.size = size;
    }

    // Getter methods
    public String getColor() {
        return color;
    }

    public int getSize() {
        return size;
    }
}
